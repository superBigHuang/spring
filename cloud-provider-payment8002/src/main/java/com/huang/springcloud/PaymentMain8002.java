@SpringBootApplication
@MapperScan("com.huang.springcloud.dao")
@EnableEurekaClient
// 开启服务发现
@EnableDiscoveryClient
public class PaymentMain8002 {
    public static void main(String[] args) {
        SpringApplication.run(PaymentMain8002.class,args);
    }
}
